C     TEST07 - GOTO OPERATIONS
C     TESTS VARIOUS GOTO CONSTRUCTS (THE FORTRAN IV WAY)
C
      INTEGER I, N, CODE
      REAL X
C
      WRITE(6,100)
  100 FORMAT(' GOTO OPERATION TESTS:')
C
C     UNCONDITIONAL GOTO
      I = 0
   10 I = I + 1
      IF (I .GE. 5) GO TO 20
      WRITE(6,110) I
  110 FORMAT(' LOOP ITERATION ',I3)
      GO TO 10
   20 CONTINUE
      WRITE(6,120)
  120 FORMAT(' UNCONDITIONAL GOTO LOOP COMPLETE')
C
C     ARITHMETIC IF (THREE-WAY BRANCH)
      X = -5.0
      IF (X) 30, 40, 50
   30 WRITE(6,130)
  130 FORMAT(' X IS NEGATIVE')
      GO TO 60
   40 WRITE(6,140)
  140 FORMAT(' X IS ZERO')
      GO TO 60
   50 WRITE(6,150)
  150 FORMAT(' X IS POSITIVE')
   60 CONTINUE
C
      X = 0.0
      IF (X) 31, 41, 51
   31 WRITE(6,130)
      GO TO 61
   41 WRITE(6,140)
      GO TO 61
   51 WRITE(6,150)
   61 CONTINUE
C
      X = 5.0
      IF (X) 32, 42, 52
   32 WRITE(6,130)
      GO TO 62
   42 WRITE(6,140)
      GO TO 62
   52 WRITE(6,150)
   62 CONTINUE
C
C     COMPUTED GOTO
      DO 80 CODE = 1, 4
         GO TO (71, 72, 73, 74), CODE
   71    WRITE(6,171)
  171    FORMAT(' CODE 1: WINTER')
         GO TO 80
   72    WRITE(6,172)
  172    FORMAT(' CODE 2: SPRING')
         GO TO 80
   73    WRITE(6,173)
  173    FORMAT(' CODE 3: SUMMER')
         GO TO 80
   74    WRITE(6,174)
  174    FORMAT(' CODE 4: FALL')
   80 CONTINUE
C
      WRITE(6,999)
  999 FORMAT(' TEST07 COMPLETE')
      STOP
      END
