C     TEST09 - FORMAT SPECIFICATIONS
C     TESTS VARIOUS FORMAT DESCRIPTORS
C
      INTEGER I, J, K
      REAL X, Y
      DOUBLE PRECISION D
C
      WRITE(6,100)
  100 FORMAT(' FORMAT SPECIFICATION TESTS:')
C
C     INTEGER FORMATS
      I = 42
      J = -123
      K = 9999
      WRITE(6,110) I, J, K
  110 FORMAT(' I5:  ',I5,' ',I5,' ',I5)
      WRITE(6,111) I, J, K
  111 FORMAT(' I10: ',I10,' ',I10,' ',I10)
C
C     REAL FORMATS - F DESCRIPTOR
      X = 3.14159
      Y = -123.456
      WRITE(6,200)
  200 FORMAT(' F FORMAT:')
      WRITE(6,210) X, Y
  210 FORMAT('   F8.3:  ',F8.3,' ',F8.3)
      WRITE(6,211) X, Y
  211 FORMAT('   F10.5: ',F10.5,' ',F10.5)
C
C     REAL FORMATS - E DESCRIPTOR
      X = 1234567.0
      Y = 0.000001234
      WRITE(6,300)
  300 FORMAT(' E FORMAT (SCIENTIFIC):')
      WRITE(6,310) X, Y
  310 FORMAT('   E12.4: ',E12.4,' ',E12.4)
      WRITE(6,311) X, Y
  311 FORMAT('   E15.6: ',E15.6,' ',E15.6)
C
C     DOUBLE PRECISION - D DESCRIPTOR
      D = 3.141592653589793D0
      WRITE(6,400)
  400 FORMAT(' D FORMAT (DOUBLE PRECISION):')
      WRITE(6,410) D
  410 FORMAT('   D20.14: ',D20.14)
C
C     MIXED FORMAT WITH LITERALS
      I = 100
      X = 50.5
      WRITE(6,500) I, X
  500 FORMAT(' QUANTITY:',I5,' PRICE:',F8.2,' DOLLARS')
C
C     REPEAT COUNTS
      WRITE(6,600)
  600 FORMAT(' REPEAT FORMAT:')
      WRITE(6,610) 1, 2, 3, 4, 5
  610 FORMAT(5I5)
      WRITE(6,620) 1.1, 2.2, 3.3
  620 FORMAT(3F8.2)
C
C     SKIP AND SPACING
      WRITE(6,700)
  700 FORMAT(/,' AFTER BLANK LINE')
      WRITE(6,710)
  710 FORMAT(' X',5X,'Y',5X,'Z')
C
      WRITE(6,999)
  999 FORMAT(' TEST09 COMPLETE')
      STOP
      END
