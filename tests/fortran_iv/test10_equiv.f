C     TEST10 - EQUIVALENCE AND DATA
C     TESTS EQUIVALENCE AND DATA STATEMENTS
C
      INTEGER IA(4)
      REAL RA(4)
      EQUIVALENCE (IA(1), RA(1))
C
      INTEGER MONTHS(12)
      DATA MONTHS /31,28,31,30,31,30,31,31,30,31,30,31/
C
      REAL PI, E, SQRT2
      DATA PI/3.14159265/, E/2.71828183/, SQRT2/1.41421356/
C
      INTEGER I
      REAL X
C
      WRITE(6,100)
  100 FORMAT(' EQUIVALENCE AND DATA TESTS:')
C
C     SHOW DATA STATEMENT INITIALIZATION
      WRITE(6,110)
  110 FORMAT(' DAYS IN EACH MONTH:')
      WRITE(6,120) (MONTHS(I), I=1,12)
  120 FORMAT(12I4)
C
      WRITE(6,130)
  130 FORMAT(' MATHEMATICAL CONSTANTS:')
      WRITE(6,140) PI, E, SQRT2
  140 FORMAT('   PI    = ',F12.8)
      WRITE(6,141) E
  141 FORMAT('   E     = ',F12.8)
      WRITE(6,142) SQRT2
  142 FORMAT('   SQRT2 = ',F12.8)
C
C     DEMONSTRATE EQUIVALENCE
      WRITE(6,200)
  200 FORMAT(' EQUIVALENCE DEMONSTRATION:')
      WRITE(6,210)
  210 FORMAT(' (INTEGER AND REAL SHARE SAME MEMORY)')
C
C     SET INTEGER VALUES
      IA(1) = 1
      IA(2) = 2
      IA(3) = 3
      IA(4) = 4
      WRITE(6,220) (IA(I), I=1,4)
  220 FORMAT(' IA AS INTEGERS: ',4I12)
C
C     READ SAME MEMORY AS REAL (SHOWS BIT PATTERN REINTERPRETATION)
      WRITE(6,230) (RA(I), I=1,4)
  230 FORMAT(' RA AS REALS:    ',4E12.4)
C
C     SET A REAL VALUE
      RA(1) = 1.0
      WRITE(6,240) RA(1), IA(1)
  240 FORMAT(' SET RA(1)=1.0, IA(1) BECOMES:',E12.4,I12)
C
      WRITE(6,999)
  999 FORMAT(' TEST10 COMPLETE')
      STOP
      END
