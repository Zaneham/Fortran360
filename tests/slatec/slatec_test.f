C***FIRST EXECUTABLE STATEMENT  GAMLN
      IERR=0
      IF (Z.LE.0.0E0) GO TO 70
      IF (Z.GT.101.0E0) GO TO 10
      NZ = Z
      FZ = Z - NZ
      IF (FZ.GT.0.0E0) GO TO 10
      IF (NZ.GT.100) GO TO 10
      GAMLN = GLN(NZ)
      RETURN
   10 CONTINUE
      WDTOL = R1MACH(4)
      WDTOL = AMAX1(WDTOL,0.5E-18)
      I1M = I1MACH(11)
      RLN = R1MACH(5)*I1M
      FLN = AMIN1(RLN,20.0E0)
      FLN = AMAX1(FLN,3.0E0)
      FLN = FLN - 3.0E0
      ZM = 1.8000E0 + 0.3875E0*FLN
      MZ = ZM + 1
      ZMIN = MZ
      ZDMY = Z
      ZINC = 0.0E0
      IF (Z.GE.ZMIN) GO TO 20
      ZINC = ZMIN - NZ
      ZDMY = Z + ZINC
   20 CONTINUE
      ZP = 1.0E0/ZDMY
      T1 = CF(1)*ZP
      S = T1
      IF (ZP.LT.WDTOL) GO TO 40
      ZSQ = ZP*ZP
      TST = T1*WDTOL
      DO 30 K=2,22
        ZP = ZP*ZSQ
        TRM = CF(K)*ZP
        IF (ABS(TRM).LT.TST) GO TO 40
        S = S + TRM
   30 CONTINUE
   40 CONTINUE
      IF (ZINC.NE.0.0E0) GO TO 50
      TLG = ALOG(Z)
      GAMLN = Z*(TLG-1.0E0) + 0.5E0*(CON-TLG) + S
      RETURN
   50 CONTINUE
      ZP = 1.0E0
      NZ = ZINC
      DO 60 I=1,NZ
        ZP = ZP*(Z+(I-1))
   60 CONTINUE
      TLG = ALOG(ZDMY)
      GAMLN = ZDMY*(TLG-1.0E0) - ALOG(ZP) + 0.5E0*(CON-TLG) + S
      RETURN
C
C
   70 CONTINUE
      GAMLN = R1MACH(2)
      IERR=1
      RETURN
      END
C     R1MACH - REAL MACHINE CONSTANTS FOR IBM 360/370
C     CONFIGURED FOR IBM SYSTEM/360 FORTRAN G
C
      REAL FUNCTION R1MACH (I)
C
C     MACHINE CONSTANTS FOR THE IBM 360/370 SERIES.
C
C     R1MACH(1) = B**(EMIN-1), THE SMALLEST POSITIVE MAGNITUDE.
C     R1MACH(2) = B**EMAX*(1 - B**(-T)), THE LARGEST MAGNITUDE.
C     R1MACH(3) = B**(-T), THE SMALLEST RELATIVE SPACING.
C     R1MACH(4) = B**(1-T), THE LARGEST RELATIVE SPACING.
C     R1MACH(5) = LOG10(B)
C
C     IBM 360/370: BASE 16, 6 HEX DIGITS, EXPONENT RANGE -64 TO 63
C
C     USE INTEGER ARRAY WITH EQUIVALENCE TO SET BIT PATTERNS
C
      INTEGER IMACH(5)
      REAL RMACH(5)
      EQUIVALENCE (RMACH(1),IMACH(1))
C
C     IBM 360/370 HEXADECIMAL FLOATING POINT VALUES
C     STORED AS INTEGERS, ACCESSED AS REALS VIA EQUIVALENCE
C
C     IMACH(1) = Z'00100000' = SMALLEST POSITIVE = 16**(-65)
C     IMACH(2) = Z'7FFFFFFF' = LARGEST = 16**63 * (1 - 16**(-6))
C     IMACH(3) = Z'3B100000' = SMALLEST REL SPACING = 16**(-6)
C     IMACH(4) = Z'3C100000' = LARGEST REL SPACING = 16**(-5)
C     IMACH(5) = Z'41134413' = LOG10(16) = 1.20412...
C
      DATA IMACH(1) / 1048576 /
      DATA IMACH(2) / 2147483647 /
      DATA IMACH(3) / 990904320 /
      DATA IMACH(4) / 1007681536 /
      DATA IMACH(5) / 1092895763 /
C
      IF (I .LT. 1 .OR. I .GT. 5) GO TO 10
      R1MACH = RMACH(I)
      RETURN
C
   10 CONTINUE
      WRITE(6,100) I
  100 FORMAT(' R1MACH - I OUT OF BOUNDS:',I10)
      R1MACH = 0.0
      RETURN
      END
